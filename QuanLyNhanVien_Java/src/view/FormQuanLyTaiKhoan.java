    private void chuyenDoi(){
        String giaTri= tableQuanLyTaiKhoan.getValueAt(tableQuanLyTaiKhoan.getRowCount()-1, 0).toString().trim();
        // int mand=Integer.parseInt(giaTri);
        //System.out.println("dong so:"+ (tableQuanLyTaiKhoan.getRowCount()-1));
        //System.out.println("gia tri cua dong do:"+tableQuanLyTaiKhoan.getValueAt(tableQuanLyTaiKhoan.getRowCount()-1, 0).toString());
        
        int mand = 0;
        try {
        mand = Integer.parseInt(giaTri);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ồ, không thể chuyển đổi sang số nguyên, vui lòng liên hệ với nhà phát triển");
        }
       // System.out.println(mand);
        mand+=1;
        String mAnd=Integer.toString(mand);
        txtMaND.setText(mAnd);
        txtMaNV.setText("NV0"+mAnd);
    }
    private void getDuLieu(){
        NguoiDungController ndController = new NguoiDungController();
        dsnd=ndController.hienThiNguoiDung();
        DefaultTableModel model = (DefaultTableModel)tableQuanLyTaiKhoan.getModel();
        model.setRowCount(0);
        for(NguoiDung o:dsnd){
            model.addRow(new Object[]{
                o.getMaND(),
                o.getTenDangNhap(),
                o.getMatKhau(),
                o.getVaiTro()
            });
        }
        txtMaND.setEditable(false);
        txtMaNV.setEditable(false);
    }
