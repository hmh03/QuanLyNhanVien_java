    private void btnThemTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTaiKhoanActionPerformed
        // TODO add your handling code here:
        
        int loiNgaySinh=0;
        int loiTenTaiKhoan=0;
        int loiMatKhau=0;
        
        NhanVienController nvController=new NhanVienController();
        NguoiDungController ndController=new NguoiDungController();
        
        String MaND= txtMaND.getText();
        String TenDangNhap=txtTenDangNhap.getText();
        String MatKhau=txtMatKhau.getText();
        String MaNV=txtMaNV.getText();
        
        for(int i=0;i<tableQuanLyTaiKhoan.getRowCount()-1;i++){
            String tenDangnhap = tableQuanLyTaiKhoan.getValueAt(i, 1).toString().trim();
            if(TenDangNhap.trim().equals("")||TenDangNhap.trim().equals(tenDangnhap)){
                loiTenTaiKhoan++;
            }
        }
        if(MatKhau.trim().equals("")){
            loiMatKhau++;
        }
        if(loiTenTaiKhoan!=0){
            JOptionPane.showMessageDialog(this, "Tên tài khoản rỗng hoặc đã tồn tại","Thông báo",1);
            loiTenTaiKhoan=0;
        }else{
            if(loiMatKhau!=0){
                JOptionPane.showMessageDialog(this, "Mật khẩu rỗng, vui lòng nhập mật khẩu","Thông báo",1);
                loiMatKhau=0;
            }else{
                
        String VaiTro=null;
        if(radioAdmin.isSelected()){
            VaiTro="Admin";
        }else{
             VaiTro="User";
        }
        
        String Ten= txtTen.getText();
        String ngaysinh=txtNgaySinh.getText();
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date NgaySinh = null;
        try {
            NgaySinh = dateFormat.parse(ngaysinh);
        } catch (ParseException ex) {
            //Logger.getLogger(FormSuaThongTinAdmin.class.getName()).log(Level.SEVERE, null, ex);
            loiNgaySinh++;
        }
        String DiaChi=txtDiaChi.getText();
        String STK=txtSTK.getText();
        String SDT=txtSDT.getText();
        String Email=txtEmail.getText();
        String ChucVu=cmbChucVu.getSelectedItem().toString();
        String GioiTinh=null;
        if(radioNam.isSelected()){
             GioiTinh="Nam";
        }else{
             GioiTinh="Nữ";
        }
                if(loiNgaySinh!=0){
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày sinh theo định dạng (ngày/tháng/năm).\nVD:04/10/2003","Thông báo",1);
                }else{
                    if(ndController.themNguoiDung(MaND, TenDangNhap, MatKhau, VaiTro)>0&&nvController.themNhanVien(MaNV, MaND, Ten, NgaySinh, DiaChi, GioiTinh, STK, SDT, Email, ChucVu)>0){
                    JOptionPane.showMessageDialog(this, "Thêm Thành Công","Thông báo",1);   
                    getDuLieu();
                    }else{
                        JOptionPane.showMessageDialog(this, "Thêm Thất Bại","Thông báo",0);
                    }
                }
            }
        }
        

    }//GEN-LAST:event_btnThemTaiKhoanActionPerformed

    private void btnXoaTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaTaiKhoanActionPerformed
        // TODO add your handling code here:
        int index=tableQuanLyTaiKhoan.getSelectedRow();
        NguoiDungController ndController=new NguoiDungController();
         int tk = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa '"+txtTen.getText().trim()+ "' không?","Xóa",JOptionPane.YES_OPTION);
      if (tk==JOptionPane.YES_OPTION)  
      {
        if (ndController.xoaNguoiDung(tableQuanLyTaiKhoan.getValueAt(index,0).toString()))
        {
            JOptionPane.showMessageDialog(this, "Xóa tài khoản thành công", "Thông Báo", 1);
            getDuLieu();
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi hệ thống", "Thông Báo", 0);
        }
        
      }
      else
      {
          return; 
      }
    }//GEN-LAST:event_btnXoaTaiKhoanActionPerformed

