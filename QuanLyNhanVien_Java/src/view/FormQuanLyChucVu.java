    private void tableChucVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableChucVuMouseClicked
        // TODO add your handling code here:
        int row=tableChucVu.getSelectedRow();
        String chucVu=tableChucVu.getValueAt(row, 0).toString();
        String heSoLuong=tableChucVu.getValueAt(row, 1).toString();
        Float hsl=Float.valueOf(heSoLuong);
        
        txtChucVu.setText(chucVu);
        txtChucVu.setCaretPosition(0);
        txtHeSoLuong.setText(Float.toString(hsl));
        
        if(row==0){
        btnThem.setEnabled(false);
        btnSua.setEnabled(false);
        txtChucVu.setEditable(false);
        btnXoa.setEnabled(false);
        txtHeSoLuong.setEditable(false);
        }else{
            btnThem.setEnabled(false);
        btnSua.setEnabled(true);
        txtChucVu.setEditable(false);
        btnXoa.setEnabled(true);
        txtHeSoLuong.setEditable(true);
        }
    }//GEN-LAST:event_tableChucVuMouseClicked

