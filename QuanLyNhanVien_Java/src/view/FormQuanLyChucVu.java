    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String chucVu=txtChucVu.getText().toString().trim();
        int loiChucVu=0;
        for(int i=0;i<tableChucVu.getRowCount()-1;i++){
            if(tableChucVu.getValueAt(i, 0).toString().trim().equalsIgnoreCase(chucVu)){
                loiChucVu++;
            }
        }
        if(chucVu.equals("")){
            loiChucVu++;
        }
        Float heSoLuong=null;
        int loiHeSoLuong=0;
        try {
            heSoLuong=Float.valueOf(txtHeSoLuong.getText());
        } catch (Exception e) {
            loiHeSoLuong++;
        }
        ChucVuController cvController=new ChucVuController();
        if(loiHeSoLuong==0){
            
            if(loiChucVu==0){
                if(cvController.themChucVu(chucVu, heSoLuong)>0){
                JOptionPane.showMessageDialog(this, "Thêm mới thành công","Chúc mừng",1);
                getDuLieu();
                }else{
                JOptionPane.showMessageDialog(this, "Thêm mới thất bại", "Rất tiếc", 1);
                }
            }else{
                 JOptionPane.showMessageDialog(this, "Chức vụ này có vẻ đã tồn tại", "Thông báo", 1);
            }  
        }else{
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số thực vào ô hệ số lương", "Thông báo", 1);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String chucVu=txtChucVu.getText().toString().trim();
        Float heSoLuong=null;
        int loiHeSoLuong=0;
        try {
            heSoLuong=Float.valueOf(txtHeSoLuong.getText());
        } catch (Exception e) {
            loiHeSoLuong++;
        }
        ChucVuController cvController=new ChucVuController();
        if(loiHeSoLuong==0){
            if(cvController.suaChucVu(chucVu, heSoLuong)>0){
                JOptionPane.showMessageDialog(this, "Sửa thành công","Chúc mừng",1);
                getDuLieu();
            }else{
                JOptionPane.showMessageDialog(this, "Sửa thất bại", "Rất tiếc", 1);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số thực vào ô hệ số lương", "Thông báo", 1);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:\
        String chucVu=txtChucVu.getText().toString().trim();
        int tk = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa '"+chucVu+ "' không?","Xóa",JOptionPane.YES_OPTION);
        if(tk==JOptionPane.YES_OPTION){
            NhanVienController nvController=new NhanVienController();
            ChucVuController cvController=new ChucVuController();
            nvController.xoaChucVuNhanVien(chucVu);
            if(cvController.xoaChucVu(chucVu)>0){
                JOptionPane.showMessageDialog(this, "Xóa thành công\n Bạn cần vào mục Quản lý nhân viên để đổi Chức vụ cho các nhân viên từng có chức vụ vừa bị xóa", "Thành công", 1);
                getDuLieu();
            }else{
                JOptionPane.showMessageDialog(this, "Xóa thất bại", "Rất tiếc", 1);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

