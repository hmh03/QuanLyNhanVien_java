    private void getDuLieu() {
        ChucVuController cvController=new ChucVuController();
        dscv=cvController.docChucVu();
        DefaultTableModel model = (DefaultTableModel)tableChucVu.getModel();
        model.setRowCount(0);
        for(ChucVu o:dscv){
            model.addRow(new Object[]{
                o.getChucvu(),
                o.getHeSoLuong()
            });
        }
        txtChucVu.setText("");
        txtHeSoLuong.setText("");
        btnSua.setEnabled(false);
        btnXoa.setEnabled(false);
    }
