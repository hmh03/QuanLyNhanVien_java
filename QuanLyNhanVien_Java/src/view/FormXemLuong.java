    private void getData(){
        String VaiTro="User";
        NhanVienController nvController=new NhanVienController();
        dsnv=nvController.docNhanVien(VaiTro);
        DefaultTableModel model = (DefaultTableModel) tableTraLuong.getModel();
        model.setRowCount(0);
        ChucVuController cvController=new ChucVuController();
        dscv=cvController.docChucVu();

        for(NhanVien o:dsnv){
            
            float heSoLuong=0;
            for(ChucVu cv:dscv){
                if(cv.getChucvu().equals(o.getChucVu())){
                    heSoLuong=cv.getHeSoLuong();
                    break;
                }
            }
            model.addRow(new Object[]{
                o.getMaNV(),
            o.getTen(),
            o.getNgaySinh(),
            o.getSTK(),
            o.getChucVu(),
            o.getChamCong(),
            heSoLuong,
            o.getChamCong()*heSoLuong
            });
        }
    }
