    private void btnTraLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraLuongActionPerformed
        // TODO add your handling code here:
        int tk = JOptionPane.showConfirmDialog(null, "Bạn có chắc là đã trả lương cho họ rồi chứ? ","Trả lương",JOptionPane.YES_OPTION);
      if (tk==JOptionPane.YES_OPTION){
          NhanVienController nvController = new NhanVienController();
        for(int  i=0;i<tableTraLuong.getRowCount();i++){
            String maNv= tableTraLuong.getValueAt(i, 0).toString();
            int chamCong = (Boolean.TRUE.equals(tableTraLuong.getValueAt(i, 8))) ? 0 : (int) tableTraLuong.getValueAt(i, 5);
            nvController.chamCongNhanVien(chamCong, maNv);
        }
        JOptionPane.showMessageDialog(this, "Trả lương hoàn tất", "Thông báo", 1);
      }
      getData();
        
        
    }//GEN-LAST:event_btnTraLuongActionPerformed

