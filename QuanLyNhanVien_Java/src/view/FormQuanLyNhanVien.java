    public void hienThiLenTable(){
        String vaitro="User";
        NhanVienController nvController=new NhanVienController();
        dsnv=nvController.docNhanVien(vaitro);
        DefaultTableModel model = (DefaultTableModel)tableQuanLyNhanVien.getModel();
        model.setRowCount(0);
//        for(int i=0;i<dsnv.size();i++){
//            
//        }
        for (NhanVien o : dsnv) {
            model.addRow(new Object[]{
                o.getMaNV(),
                o.getTen(),
                o.getNgaySinh(),
                o.getDiaChi(),
                o.getGioiTinh(),
                o.getSTK(),
                o.getSDT(),
                o.getEmail(),
                o.getChucVu(),
                o.getChamCong()
            });
        }
        btnCapNhatChucVu.setEnabled(false);
    }
