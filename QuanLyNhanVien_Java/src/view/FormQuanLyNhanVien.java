    private void tableQuanLyNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableQuanLyNhanVienMouseClicked
        // TODO add your handling code here:
        btnCapNhatChucVu.setEnabled(true);
        int row = tableQuanLyNhanVien.getSelectedRow();
        String ten=tableQuanLyNhanVien.getValueAt(row, 1).toString();
        String ngaysinh=tableQuanLyNhanVien.getValueAt(row, 2).toString();
        String diachi=tableQuanLyNhanVien.getValueAt(row, 3).toString();
        String gioitinh=tableQuanLyNhanVien.getValueAt(row, 4).toString();
        String stk=tableQuanLyNhanVien.getValueAt(row, 5).toString();
        String sdt=tableQuanLyNhanVien.getValueAt(row, 6).toString();
        String email=tableQuanLyNhanVien.getValueAt(row, 7).toString();
        String chucvu=tableQuanLyNhanVien.getValueAt(row, 8).toString();
        String chamcong=tableQuanLyNhanVien.getValueAt(row, 9).toString();
        
        txtTen.setText(ten);
        txtTen.setCaretPosition(0);
        txtNgaySinh.setText(ngaysinh);
        txtDiaChi.setText(diachi);
        txtDiaChi.setCaretPosition(0);
        if(gioitinh.equalsIgnoreCase("Nam")){
            radioNam.setSelected(true);
        }else{
            radioNu.setSelected(true); 
        }txtSTK.setText(stk);
        txtSTK.setCaretPosition(0);
        txtSDT.setText(sdt);
        txtSDT.setCaretPosition(0);
        txtEmail.setText(email);
        txtEmail.setCaretPosition(0);
        cmbChucVu.setSelectedItem(chucvu);
        txtChamCong.setText(chamcong);
        
        txtTen.setEditable(false);
        txtNgaySinh.setEditable(false);
        txtDiaChi.setEditable(false);
        txtSTK.setEditable(false);
        txtSDT.setEditable(false);
        txtEmail.setEditable(false);
        radioNam.setEnabled(false);
        radioNu.setEnabled(false);
        txtChamCong.setEditable(false);
    }//GEN-LAST:event_tableQuanLyNhanVienMouseClicked

    private void btnLuuChamCongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuChamCongActionPerformed
        // TODO add your handling code here:
        NhanVienController nvController = new NhanVienController();
       // int r= tableQuanLyNhanVien.getSelectedRow();
        for(int  i=0;i<tableQuanLyNhanVien.getRowCount();i++){
            String maNv= (String) tableQuanLyNhanVien.getValueAt(i, 0).toString();
            int chamCong= ((int) tableQuanLyNhanVien.getValueAt(i, 9)+(Boolean.TRUE.equals(tableQuanLyNhanVien.getValueAt(i, 10))?1:0));
            nvController.chamCongNhanVien(chamCong, maNv);
        }
        JOptionPane.showMessageDialog(this, "Chấm công thành công", "Thông báo", 1);
        
        hienThiLenTable();
        
    }//GEN-LAST:event_btnLuuChamCongActionPerformed

